export function dayToString(i) {
    if (i == 1) {
      return "Maanantai";
    }
    if (i == 2) {
      return "Tiistai";
    }
    if (i == 3) {
      return "Keskiviikko";
    }
    if (i == 4) {
      return "Torstai";
    }
    if (i == 5) {
      return "Perjantai";
    }
    if (i == 6) {
      return "Lauantai";
    }
    if (i == 7) {
      return "Sunnuntai";
    }
  }

 